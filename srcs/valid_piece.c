int		valid_piece(char *str)
{
	int		i;
	int		j;
	int		k;

	i = 0;
	k = 0;
	j = 0;
	while (str[i] == '.' || str[i] == '#' || str[i] == '\n')
	{
		if (str[i] == '#')
		{
			k++;
			if (str[i + 1] == '#' || str[i + 5] == '#' || j == 3)
				j++;
		}
		i++;
	}
	if (j != 4 || k != 4 || i != 20)
		return (0);
	return (1);
}
